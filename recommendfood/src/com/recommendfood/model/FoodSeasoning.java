package com.recommendfood.model;

public class FoodSeasoning {
	private String seasoningName;
	private String seasoningAmount;
	public String getSeasoningName() {
		return seasoningName;
	}
	public void setSeasoningName(String seasoningName) {
		this.seasoningName = seasoningName;
	}
	public String getSeasoningAmount() {
		return seasoningAmount;
	}
	public void setSeasoningAmount(String seasoningAmount) {
		this.seasoningAmount = seasoningAmount;
	}
	
}
